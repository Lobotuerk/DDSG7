    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int hora = Integer.parseInt(cbHorarios.getSelectedItem().toString());
        if (txtObjetivo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el objetivo");
        } else {
            Boolean existeHora = false;
            for (Hora h : arrayHoras) {
                if (h.getNumeroOrden() == ordenProduccion.getNumeroOrden()) {
                    if (h.getHora() == hora && h.getFecha().equals(ordenProduccion.getFecha())) {
                        existeHora = true;
                    }
                }
            }
            if (existeHora) {
                JOptionPane.showMessageDialog(null, "Ya existen objetivos para la hora seleccionada");
            } else {
                int id = MisFunciones.getProximoIdHoras();
                int numeroOrden = ordenProduccion.getNumeroOrden();
                String fecha = ordenProduccion.getFecha();

                int objetivo = Integer.parseInt(txtObjetivo.getText());

                Horas horas = new Horas();
                Hora nuevaHora = new Hora(id, numeroOrden, fecha, hora, objetivo);
                horas.agregarHora(nuevaHora);

                dispose();
                VentanaGestionarOP v = new VentanaGestionarOP();
                v.seleccionarOrden(ordenProduccion);
            }

        }
    }//GEN-LAST:event_btnGuardarActionPerformed


